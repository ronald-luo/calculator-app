function add (numA, numB) {
    return numA + numB
}

function subtract (numA, numB) {
    return numA - numB
}

function multiply (numA, numB) {
    return numA * numB
}

function divide (numA, numB) {
    return numA / numB
}

function operate(numA, numB, operator) {
    return operator(numA, numB)
}